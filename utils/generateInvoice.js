const fs = require("fs");
const path = require("path");
const PDFDocument = require("pdfkit");
const QRCode = require("qrcode");

const generateInvoice = async (booking, listing, guest) => {
  return new Promise(async (resolve, reject) => {
    const invoiceDir = path.join(__dirname, "../invoices");
    if (!fs.existsSync(invoiceDir))
      fs.mkdirSync(invoiceDir, { recursive: true });

    const filePath = path.join(invoiceDir, `invoice-${booking._id}.pdf`);
    const qrPath = path.join(invoiceDir, `qr-${booking._id}.png`);
    const doc = new PDFDocument({ size: "A4", margin: 50 });

    const stream = fs.createWriteStream(filePath);
    doc.pipe(stream);

    // âœ… Load Bangla font if available
    const banglaFontPath = path.join(
      __dirname,
      "../fonts/NotoSansBengali-VariableFont_wdth,wght.ttf"
    );
    if (fs.existsSync(banglaFontPath)) {
      doc.registerFont("Bangla", banglaFontPath);
    }

    // âœ… Add logo
    const logoPath = path.join(__dirname, "../assets/banglabnb-logo.png");
    if (fs.existsSync(logoPath)) {
      doc.image(logoPath, 50, 50, { width: 100 });
    }

    doc
      .fillColor("#006a4e")
      .fontSize(22)
      .text("BanglaBnB", 200, 50, { align: "right" })
      .fontSize(14)
      .fillColor("#d21034")
      .text("ðŸ“„ Booking Invoice", { align: "right" });

    doc.moveDown().moveTo(50, doc.y).lineTo(550, doc.y).stroke();

    const nights = Math.ceil(
      (new Date(booking.dateTo) - new Date(booking.dateFrom)) /
        (1000 * 60 * 60 * 24)
    );
    const baseRate = listing.price;
    const serviceFee = 100;
    const total = baseRate * nights + serviceFee;

    const formatCurrency = (value) => `à§³${value.toFixed(2)}`;

    doc
      .moveDown()
      .fillColor("black")
      .fontSize(12)
      .text(`Booking ID: ${booking._id}`)
      .text(`Guest: ${guest.name} (${guest.email})`)
      .text(`Listing: ${listing.title}`)
      .text(`Location: ${listing.location?.address}`)
      .text(
        `Dates: ${new Date(booking.dateFrom).toLocaleDateString()} â†’ ${new Date(
          booking.dateTo
        ).toLocaleDateString()}`
      )
      .text(`Payment Status: ${booking.paymentStatus}`)
      .moveDown();

    // Bangla Translation
    doc
      .font("Bangla")
      .fillColor("#333")
      .fontSize(11)
      .text(`à¦…à¦¤à¦¿à¦¥à¦¿: ${guest.name}`, { continued: true })
      .text(`  â€¢  à¦®à§‡à¦‡à¦²: ${guest.email}`)
      .text(`à¦¸à§à¦¥à¦¾à¦¨: ${listing.location?.address}`)
      .text(
        `à¦¤à¦¾à¦°à¦¿à¦–: ${new Date(booking.dateFrom).toLocaleDateString()} â†’ ${new Date(
          booking.dateTo
        ).toLocaleDateString()}`
      )
      .moveDown();

    // Price Breakdown
    doc
      .font("Helvetica")
      .fillColor("black")
      .fontSize(13)
      .text("ðŸ’µ Price Breakdown", { underline: true });

    doc
      .fontSize(12)
      .text(`Nightly Rate (à§³${baseRate} x ${nights} nights):`, 50)
      .text(formatCurrency(baseRate * nights), 0, doc.y, { align: "right" })
      .text("Service Fee:", 50)
      .text(formatCurrency(serviceFee), 0, doc.y, { align: "right" })
      .text("Total Amount Paid:", 50, doc.y + 5)
      .font("Helvetica-Bold")
      .text(formatCurrency(total), 0, doc.y + 5, { align: "right" });

    // Invoice Number & Date
    doc.moveDown(2).font("Helvetica").fontSize(11).fillColor("gray");
    doc.text(`Invoice Number: INV-${booking._id}`);
    doc.text(`Issued on: ${new Date().toLocaleDateString()}`);

    // Generate QR
    await QRCode.toFile(
      qrPath,
      `https://banglabnb.com/bookings/${booking._id}`,
      { width: 100 }
    );
    doc.image(qrPath, 450, doc.y - 40, { width: 80 });

    // Footer
    doc.moveDown(4);
    doc
      .fontSize(10)
      .fillColor("gray")
      .text("This invoice was automatically generated by BanglaBnB", {
        align: "center",
      });

    doc.end();

    stream.on("finish", () => {
      // âœ… Return local file path for attachment
      resolve(filePath);
      fs.unlink(qrPath, () => {});
    });

    stream.on("error", reject);
  });
};

module.exports = generateInvoice;
